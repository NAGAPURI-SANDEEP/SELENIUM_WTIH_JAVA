package DAY_2;

public class Data_Types_Demo 
{

	public static void main(String[] args) 
	{
		// Numeric data types
		int a =100, b=200;
		System.out.println("The value of a is: "+a);
		System.out.println("The value of b is: "+b);
		System.out.println("The sum of a and b is:"+(a+b));
		
		//byte 
		byte d = -18;
		System.out.println(d);
		
		// short
		short e  =31234;
		System.out.println(e);
		
		//long literal needed
		long l =2143445566344385445L;
		System.out.println(l);
		
		
		//decimal numbers  float literal is needed;
		float flt = 4.8989834f;
		System.out.println(flt);
		
		double dbl = 5.8458945575934759375;
		System.out.println(dbl);
		
		//char  to store single character
		char grade = 'A';
		System.out.println(grade);
		
		// String to store multiple characters, In double quotes
		String name = "JHON";
		System.out.println(name);
		
		//   char ch="f";
		//   String nm='jhon';
		//   String nm='A'
		
		String ch="J";
		
		// boolean is used to represent the status 
		boolean Is_working = true;
		boolean is_present = false;
		
		System.out.println(Is_working);
		System.out.println(is_present);
		
		// boolean bl ="true";
		// boolean bl ="false";
		// String bl =true;
		// String bl =false;
		
		String bl ="true";
		System.out.println(bl);
		
		
		
		
		
				
		

	}

}
